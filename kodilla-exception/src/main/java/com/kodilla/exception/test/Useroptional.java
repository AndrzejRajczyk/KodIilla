public class Useroptional {
    String s;
    int i;
    int n;
    String d;

    public Useroptional(String s, int i, int n, String d) {
        this.s = s;
        this.i = i;
        this.n = n;
        this.d = d;
    }

    public String getS() {
        return s;
    }

    public int getUsername() {
        return i;
    }

    public int getN() {
        return n;
    }

    public String getD() {
        return d;
    }
}
